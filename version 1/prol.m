function [u1,v1,p1] = prol(u,v,p)
%提升算子
n = size(p,1);
n0 = 2*n;
u1 = zeros(n0,n0+1);
v1 = zeros(n0+1,n0);
p1 = zeros(n0,n0);

%提升u,f
%上下边界
%粗网格
u1(1,2:2:n0) = (u(1,1:n) + u(1,2:n+1))/2;
u1(n0,2:2:n0) = (u(n,1:n) + u(n,2:n+1))/2;
%细网格
u1(1,1:2:n0+1) = u(1,n+1);
u1(n0,1:2:n0+1) = u(n,n+1);
%内部（除了上下边界）
%粗网格
u1(2:2:n0-1,1:2:n0+1) = (3*u(1:n-1,1:n+1) + u(2:n,1:n+1))/4;
u1(3:2:n0-1,1:2:n0+1) = (u(1:n-1,1:n+1) + 3*u(2:n,1:n+1))/4;
%细网格
u1(2:2:n0-1,2:2:n0) = (3*u(1:n-1,1:n) + 3*u(1:n-1,2:n+1) + u(2:n,1:n) + u(2:n,2:n+1))/8;
u1(3:2:n0-1,2:2:n0) = (u(1:n-1,1:n) + u(1:n-1,2:n+1) + 3*u(2:n,1:n) + 3*u(2:n,2:n+1))/8;

%提升v,g
%左右边界
%粗网格
v1(2:2:n0,1) = (v(1:n,1) + v(2:n+1,1))/2;
v1(2:2:n0,n0) = (v(1:n,n) + v(2:n+1,n))/2;
%细网格
v1(1:2:n0+1,1) = v(n+1,1);
v1(1:2:n0+1,n0) = v(n+1,n);
%内部（除了左右边界）
%粗网格
v1(1:2:n0+1,2:2:n0-1) = (3*v(1:n+1,1:n-1) + v(1:n+1,2:n))/4;
v1(1:2:n0+1,3:2:n0-1) = (v(1:n+1,1:n-1) + 3*v(1:n+1,2:n))/4;
%细网格
v1(2:2:n0,2:2:n0-1) = (3*v(1:n,1:n-1) + 3*v(2:n+1,1:n-1) + v(1:n,2:n) + v(2:n+1,2:n))/8;
v1(2:2:n0,3:2:n0-1) = (v(1:n,1:n-1) + v(2:n+1,1:n-1) + 3*v(1:n,2:n) + 3*v(2:n+1,2:n))/8;

%提升p
%顶点
p1(1,1) = p(1,1);
p1(n0,1) = p(n,1);
p1(1,n0) = p(1,n);
p1(n0,n0) = p(n,n);
%边界
p1(1,2:2:n0-1) = (3*p(1,1:n-1) + p(1,2:n))/2;
p1(1,3:2:n0-1) = (p(1,1:n-1) + 3*p(1,2:n))/2;
p1(n0,2:2:n0-1) = (3*p(n,1:n-1) + p(n,2:n))/2;
p1(n0,3:2:n0-1) = (p(n,1:n-1) + 3*p(n,2:n))/2;
p1(2:2:n0-1,1) = (3*p(1:n-1,1) + p(2:n,1))/2;
p1(3:2:n0-1,1) = (p(1:n-1,1) + 3*p(2:n,1))/2;
p1(2:2:n0-1,n0) = (3*p(1:n-1,n) + p(2:n,n))/2;
p1(3:2:n0-1,n0) = (p(1:n-1,n) + 3*p(2:n,n))/2;
%内部
p1(2:2:n0-1,2:2:n0-1) = (9*p(1:n-1,1:n-1) + 3*p(1:n-1,2:n) + 3*p(2:n,1:n-1) + p(2:n,2:n))/16;
p1(3:2:n0-1,2:2:n0-1) = (3*p(1:n-1,1:n-1) + 3*p(1:n-1,2:n) + 9*p(2:n,1:n-1) + p(2:n,2:n))/16;
p1(2:2:n0-1,3:2:n0-1) = (3*p(1:n-1,1:n-1) + 9*p(1:n-1,2:n) + 3*p(2:n,1:n-1) + p(2:n,2:n))/16;
p1(3:2:n0-1,3:2:n0-1) = (p(1:n-1,1:n-1) + 3*p(1:n-1,2:n) + 3*p(2:n,1:n-1) + 9*p(2:n,2:n))/16;
end