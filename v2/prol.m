function [u1,v1,p1] = prol(u,v,p)
%ÌáÉýËã×Ó
n = size(p,1);
n0 = 2*n;
u1 = zeros(n0,n0+1);
v1 = zeros(n0+1,n0);
p1 = zeros(n0,n0);

u1(1,1:2:n0+1) = u(1,:)/2;
u1(n0,1:2:n0+1) = u(n,:)/2;
u1(2:2:n0-1,1:2:n0+1) = (3*u(1:n-1,:) + u(2:n,:))/4;
u1(3:2:n0,1:2:n0+1) = (u(1:n-1,:) + 3*u(2:n,:))/4;
u1(:,2:2:n0) = (u1(:,1:2:n0) + u1(:,3:2:n0+1))/2;

v1(1:2:n0+1,1) = v(:,1)/2;
v1(1:2:n0+1,n0) = v(:,n)/2;
v1(1:2:n0+1,2:2:n0-1) = (3*v(:,1:n-1) + v(:,2:n))/4;
v1(1:2:n0+1,3:2:n0) = (v(:,1:n-1) + 3*v(:,2:n));
v1(2:2:n0,:) = (v1(1:2:n0,:) + v1(3:2:n0+1,:))/2;

p1(1:2:n0,1:2:n0) = p;
p1(2:2:n0,1:2:n0) = p;
p1(1:2:n0,2:2:n0) = p;
p1(2:2:n0,2:2:n0) = p;
end